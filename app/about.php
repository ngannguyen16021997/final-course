   	<h1>Giới Thiệu Sản Phẩm Chúng Tôi</h1>
   	  Đến với một vài thông tin mà chúng tôi muốn giới thiệu về máy ảnh nikon và canon sau đây sẽ là nguồn thông tin vô cùng thú vị dành cho các bạn.
Ở Việt Nam, số người dùng máy ảnh Canon khá lớn và những người này tiếp tục tuyên truyền đến các thế hệ sau, nên số người dùng máy ảnh Canon ngày càng hùng hậu hơn so với Nikon. Nhưng sẽ là oan cho Nikon nếu bảo rằng chất lượng máy ảnh Nikon không bằng Canon.
Trên các máy DSLR cấp thấp (entry level), máy ảnh Canon thường đặt tất cả các nút bấm cài đặt chính và các vòng xoay chỉnh chế độ thông dụng ở xung quanh màn hình LCD, nơi người dùng "mới bắt đầu" có thể dễ dàng tìm thấy cái họ cần. Trong khi đó, Nikon yêu cầu người dùng thực hiện một số bước để thực hiện thay đổi các cài đặt. Tuy nhiên, khi vào đến phần thiết lập, việc sử dụng máy rất dễ dàng. Do đó, bạn cần dành thời gian để tìm hiểu máy ảnh của bạn. Nếu bạn chưa hài lòng với chiếc máy đang dùng, hãy xem lại cách sử dụng máy của bạn.
Tìm hiểu sự khác biệt lớn nhất giữa hai thương hiệu máy ảnh Canon và máy ảnh Nikon nằm ở các ống kính.

   <div style="text-align: center;"><img src="../public/uploads/may7.jpg"></div>

Sự đối đầu giữa máy ảnh Nikon và máy ảnh Canon được xem là một cuộc chiến đấu dai dẳng. Lý do để ban đầu mà người dùng chọn mua máy ảnh Nikon hay Canon thường là do mẫu máy họ thích đang có giảm giá hấp dẫn trong khi các thông số máy đều rất thuyết phục hoặc một lý do nào đó rất cá nhân. Sau đó thì họ đầu tư cho các ống kính tương thích với chiếc máy vừa mua, đến khi muốn nâng cấp máy, chẳng có lý do gì mà họ lại chọn máy ảnh thương hiệu khác để lại phải sắm từ đầu cho bộ ống kính mà không tận dụng được số ống kính cũ. Hơn thế nữa, chất lượng chụp ảnh của cả hai thương hiệu là gần như tương đương nhau, nên ít ai không hài lòng để tìm đến thương hiệu kia. Ngoài ra, cũng có rất nhiều người sở hữu cùng lúc cả hai máy ảnh Nikon và máy ảnh Canon, vì nhiều lý do khác nhau.
Một khác biệt lớn nữa giữa Canon và Nikon là "triết lý sản xuất" của họ. Theo một bài viết trên vnphoto, thì Nikon thường chọn con đường tập trung vào những nghiên cứu, phát triển công nghệ mới, còn Canon chọn hướng nghiên cứu thiên về lợi nhuận, phù hợp tác động của thị trường. Canon phát triển bằng cách nâng cao lên các thông số kỹ thuật, chứ không phải là thay đổi bản chất các thông số đó. Trong khi Nikon phát triển công nghệ cảm biến mới thì Canon sẽ hài lòng với công nghệ đã thành công, và chỉ cần tăng thêm vài thông số về độ phân giải hay tốc độ. Nikon liên tục áp dụng những công nghệ mới, cải tiến mới biến "cái không thể thành có thể". Cải tiến của Canon thì đảm bảo cho ra những sản phẩm phù hợp những tính toán thị trường. Canon chỉ áp dụng công nghệ CMOS đã thành công nhiều năm trong các nhà máy sản xuất sensor của mình. Còn Nikon sử dụng rất nhiều công nghệ khi sản xuất máy ảnh: CCD, CMOS, JFET LBCAST… mỗi loại đều có đặc trưng riêng, hạn chế, chức năng riêng. Với Canon là ưu thế về giá cả, còn Nikon thì có sự tự do hơn…
